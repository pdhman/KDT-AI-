    "                                      'stop_loss' if stop_loss else 'take_profit',\n",
    "                        'holding_days': (current['date'] - position['entry_date']).days\n",
    "                    })\n",
    "                    \n",
    "                    cash += sell_amount\n",
    "                    position = None\n",
