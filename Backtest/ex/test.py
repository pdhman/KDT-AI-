    "class BacktestEngine:\n",
    "    \"\"\"모멘텀 전략 백테스트 엔진\"\"\"\n",
    "    \n",
    "    def __init__(self, initial_capital=10000000, commission=0.00015):\n",
    "        \"\"\"\n",
    "        initial_capital: 초기 자본 (기본 1000만원)\n",
