    "        \"\"\"\n",
    "        self.initial_capital = initial_capital\n",
    "        self.commission = commission\n",
    "        self.strategy = MomentumStrategy()\n",
    "        \n",
    "    def run_backtest(self, ticker: str, df: pd.DataFrame, \n",
    "                     position_size=0.3) -> Dict:\n",
    "        \"\"\"\n",
    "        단일 종목 백테스트\n",
    "        position_size: 포지션 비율 (기본 30%)\n",
    "        \"\"\"\n",
    "        # 지표 계산\n",
    "        df['cci'] = self.strategy.calculate_cci(df)\n",
    "        df['macd'], df['signal'], df['histogram'] = self.strategy.calculate_macd(df)\n",
    "        df['bb_upper'], df['bb_middle'], df['bb_lower'] = self.strategy.calculate_bollinger_bands(df)\n",
    "        \n",
    "        # 거래 기록\n",
    "        trades = []\n",
    "        position = None  # 현재 포지션\n",
    "        cash = self.initial_capital\n",
    "        \n",
    "        for i in range(50, len(df)):  # 충분한 데이터 확보 후 시작\n",
    "            current = df.iloc[i]\n",
    "            previous = df.iloc[i-1]\n",
    "            \n",
    "            # 매수 신호\n",
    "            if position is None:\n",
    "                cci_breakout = current['cci'] > 100 and previous['cci'] <= 100\n",
    "                macd_golden = (current['macd'] > current['signal'] and \n",
    "                              previous['macd'] <= previous['signal'])\n",
    "                price_above_bb = (current['close'] > current['bb_middle'] and \n",
    "                                 previous['close'] <= previous['bb_middle'])\n",
    "                \n",
    "                if cci_breakout and macd_golden and price_above_bb:\n",
