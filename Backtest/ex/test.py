    "        \n",
    "        profit_factor = abs(avg_win * winning_trades / (avg_loss * losing_trades)) if losing_trades > 0 else float('inf')\n",
    "        \n",
    "        total_profit = df_trades['profit'].sum()\n",
    "        avg_return = df_trades['return'].mean()\n",
    "        max_return = df_trades['return'].max()\n",
    "        min_return = df_trades['return'].min()\n",
    "        \n",
    "        avg_holding_days = df_trades['holding_days'].mean()\n",
    "        \n",
    "        return {\n",
    "            'total_trades': total_trades,\n",
    "            'winning_trades': winning_trades,\n",
    "            'losing_trades': losing_trades,\n",
