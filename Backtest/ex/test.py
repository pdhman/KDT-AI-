    "class MomentumStrategy:\n",
    "    \"\"\"모멘텀 지표 계산 클래스\"\"\"\n",
    "    \n",
    "    def calculate_cci(self, df: pd.DataFrame, period=20) -> pd.Series:\n",
    "        \"\"\"CCI (Commodity Channel Index) 계산\"\"\"\n",
    "        tp = (df['high'] + df['low'] + df['close']) / 3\n",
    "        sma = tp.rolling(window=period).mean()\n",
    "        mad = tp.rolling(window=period).apply(lambda x: np.abs(x - x.mean()).mean())\n",
    "        cci = (tp - sma) / (0.015 * mad)\n",
    "        return cci\n",
    "    \n",
    "    def calculate_macd(self, df: pd.DataFrame, \n",
    "                       fast=12, slow=26, signal=9) -> Tuple[pd.Series, pd.Series, pd.Series]:\n",
    "        \"\"\"MACD 계산\"\"\"\n",
    "        exp1 = df['close'].ewm(span=fast, adjust=False).mean()\n",
    "        exp2 = df['close'].ewm(span=slow, adjust=False).mean()\n",
    "        macd = exp1 - exp2\n",
    "        signal_line = macd.ewm(span=signal, adjust=False).mean()\n",
    "        histogram = macd - signal_line\n",
    "        return macd, signal_line, histogram\n",
    "    \n",
    "    def calculate_bollinger_bands(self, df: pd.DataFrame, \n",
    "                                  period=20, std=2) -> Tuple[pd.Series, pd.Series, pd.Series]:\n",
    "        \"\"\"볼린저 밴드 계산\"\"\"\n",
    "        middle = df['close'].rolling(window=period).mean()\n",
    "        std_dev = df['close'].rolling(window=period).std()\n",
    "        upper = middle + (std_dev * std)\n",
    "        lower = middle - (std_dev * std)\n",
    "        return upper, middle, lower"
